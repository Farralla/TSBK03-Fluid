	/**
	 * Locate the cube in a direction according to direction
	 * @param dir
	 * @return
	 */
	public static Vector3f vecInDir(int dir){
		Vector3f temp = new Vector3f();
		switch(dir){
		case LEFT:
			temp.set(-mSize,0,0);
		case RIGHT:
			temp.set(mSize,0,0);
		case DOWN:
			temp.set(0,-mSize,0);
		case UP:
			temp.set(0,mSize,0);
		case IN:
			temp.set(0,0,-mSize);
		case OUT:
			temp.set(0,0,mSize);
		}
		return temp;
	}
