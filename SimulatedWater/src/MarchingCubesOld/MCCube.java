	private Vector3f computeEdge(MCVertex v1, MCVertex v2){
		float length = (isoValue-v1.weight)/(v2.weight-v1.weight);
		if(length >= 0 && length <= 1){
			Vector3f vDir = Vector3f.sub(v2.pos, v1.pos, null);
			return (Vector3f.add(
					v1.pos,
					(Vector3f) vDir.scale(length),
					null));
		}
		return null;
	}

